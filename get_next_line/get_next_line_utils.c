#include "get_next_line.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strjoin(const char *s1, const char *s2)
{
	char	*str;
	size_t	str_size;
	size_t	i;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	str_size = (ft_strlen(s1) + ft_strlen(s2) + 1);
	str = malloc(str_size);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	ft_strlcpy(str + i, s2, str_size);
	return (str);
}

char	*ft_strchr(const char *s, int c)
{
	unsigned int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i++;
	}
	if ((char)c == '\0')
		return ((char *)&s[i]);
	return (NULL);
}

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	src_len;

	i = 0;
	src_len = ft_strlen(src);
	if (size > 0)
	{
		while (i < size - 1 && src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (src_len);
}

char	*ft_strdup(const char *s)
{
	char	*new;
	size_t	buff_size;

	buff_size = (ft_strlen(s) + 1);
	new = malloc(buff_size);
	if (new == NULL)
		return (NULL);
	ft_strlcpy(new, s, buff_size);
	return (new);
}
